package Tema4;

//Para el bucle Do While, deberÃ¡s crear la misma estructura que en el While, pero solo se debe ejecutar una vez.

public class DoWhile {   

    public static void main(String[] args) {

        int numeroWhile = 0;

        do {
            numeroWhile++;
            System.out.println(numeroWhile);   
        } while (numeroWhile > 0);
    }
}
